const nome = prompt ('digite seu nome completo:');
window.document.body.innerHTML += `Seu nome é ${nome}<br>`;
window.document.body.innerHTML += `Seu nome tem ${nome.length} letras<br>`;
window.document.body.innerHTML += `A segunda letra do seu nome é: ${nome.charAt(1)}<br>`;
window.document.body.innerHTML += `O primeiro indice da letra 'E' do seu nome é: ${nome.indexOf("E")}<br>`;
window.document.body.innerHTML += `O ultimo indice da letra "e" do seu nome é: ${nome.lastIndexOf("e")}<br>`;
window.document.body.innerHTML += `As ultimas 3 letras do seu nome são: ${nome.slice(-3)}<br>`;
window.document.body.innerHTML += `As palavras do seu nome são: ${nome.split(" ")}<br>`;
window.document.body.innerHTML += `Seu nome com letras maisusculas é:${nome.toUpperCase()}<br>`;
window.document.body.innerHTML += `Seu nome com letras minusculas é: ${nome.toLocaleLowerCase()}<br>`;
